<script lang="babel">
  export default{
    name: 'profitDet',
    data(){
      return {
        pickerOptions: {
          disabledDate: function (time) {
            return time.getTime() > Date.now() - 8.64e7;
          },
          onPick: function ({maxDate, minDate}) {
            if (maxDate == '' || maxDate == null) {
              this.disabledDate = function (maxDate) {
                return minDate < maxDate.getTime() - 8.64e7 * 30 || minDate.getTime() > maxDate || maxDate > new Date().setTime(new Date().getTime()-24*60*60*1000) || minDate > new Date().setTime(new Date().getTime()-24*60*60*1000);
              }
            } else {
              this.disabledDate= function (time) {
                return time.getTime() > Date.now() - 8.64e7;
              }
            }
          }
        },
        query:{
          pageNo:1,
          pageSize:10,
          orderNo:'',
          receiptMoneyUserName:'',
          businessType:'',
          splitDate:'',
          startTime:'',
          endTime:''
        },
        date:'',
        records: [],
        count: 0,
        total: '',
        price: '',
        loading: true,
        path:'',
        isShow:true,
        totalUrl:'',
        pageTotal:0,
        pageTotal1:0,
        addTotal:0,
        addTotal1:0,
        isDet:true
      }
    },
    created: function () {
      this.currentDate();
      if(this.$route.path=="/admin/record/profitDet"){
        this.$data.path = '/admin/queryProfit/profitDetails';
        this.$data.totalUrl = '/admin/queryProfit/profitAmount'
        this.isDet = false
      }else if(this.$route.path=="/admin/details/profitComDet"){
        this.$data.path = '/admin/allProfit/companyProfitDetail';
        this.$data.totalUrl = '/admin/allProfit/ProfitDetailAmount';
        this.$data.query.accId = this.$route.query.id;
        this.$data.query.splitDate = this.$route.query.time;
        this.$data.query.businessType = this.$route.query.type;
        this.query.startTime = this.$route.query.time.slice(0,10);
        this.query.endTime = this.$route.query.time.slice(11);
        this.isShow =false
      }else if(this.$route.path=="/admin/details/profitFirDet"){
        this.$data.path = '/admin/allProfit/firstDealerDetail';
        this.$data.totalUrl = '/admin/allProfit/firstDetailAmount';
        this.isShow =false;
        this.$data.query.receiptMoneyAccountId = this.$route.query.id;
        this.$data.query.businessType = this.$route.query.type;
        this.$data.query.splitDate = this.$route.query.time;
      }else if(this.$route.path=="/admin/details/profitSecDet"){
        this.$data.path = '/admin/allProfit/secondDealerDetail';
        this.$data.totalUrl = '/admin/allProfit/secondDetailAmount';
        this.$data.query.receiptMoneyAccountId = this.$route.query.id;
        this.$data.query.splitDate = this.$route.query.time;
        this.$data.query.businessType = this.$route.query.type;
        this.isShow =false
      }
      this.getData();
      this.getAddTotal()
    },
    methods: {
      datetimeSelect: function (val) {
        if (val == undefined) {
          this.query.startTime = '';
          this.query.endTime = '';
        } else {
          let format = val.split(' - ');
          this.query.startTime = format[0];
          this.query.endTime = format[1];
        }
      },
      currentDate: function () {
        let time = new Date();
        time.setTime(time.getTime()-24*60*60*1000);
        this.date = [time,time];
        for (var j = 0; j < this.date.length; j++) {
          var str = this.date[j];
          var ary = [str.getFullYear(), str.getMonth() + 1, str.getDate()];
          for (var i = 0, len = ary.length; i < len; i++) {
            if (ary[i] < 10) {
              ary[i] = '0' + ary[i];
            }
          }
          str = ary[0] + '-' + ary[1] + '-' + ary[2];
          if (j == 0) {
            this.$data.query.startTime = str;
          } else {
            this.$data.query.endTime = str;
          }
        }
      },
      reset: function () {
        this.query = {
          pageNo:1,
          pageSize:10,
          orderNo:'',
          receiptMoneyUserName:'',
          businessType:'',
          splitDate:'',
          startTime:'',
          endTime:''
        };
        this.currentDate()
      },
      getData: function () {
        this.loading = true;
        this.$http.post(this.path, this.query)
          .then(function (res) {
            setTimeout(()=>{
              this.loading = false;
              this.records = res.data.records;
            },1000)
            this.count = res.data.count;
            var toFix = function (val) {
              return parseFloat(val).toFixed(2)
            };
            var total=0,price = 0;
            for (let i = 0; i < res.data.records.length; i++) {
              res.data.records[i].splitTotalAmount = toFix(res.data.records[i].splitTotalAmount);
              res.data.records[i].splitAmount = toFix(res.data.records[i].splitAmount);
              total = toFix(parseFloat(total)+parseFloat(res.data.records[i].splitTotalAmount))
              price = toFix(parseFloat(price)+parseFloat(res.data.records[i].splitAmount))
            }
            this.pageTotal = total;
            this.pageTotal1 = price;
          }, function (err) {
            setTimeout(()=>{
              this.loading = false;
            },1000)
            this.$message({
              showClose: true,
              message: err.statusMessage,
              type: 'error'
            });
          })
      },
      getAddTotal(){
        this.$http.post(this.totalUrl,this.query)
          .then(res=>{
            this.addTotal = res.data.splitTotalAmount;
            this.addTotal1 = res.data.splitAmount;
          })
          .catch(err=>{
            this.$message({
              showClose: true,
              message: err.statusMessage,
              type: 'error'
            });
          })
      },
      search(){
        this.total = '';
        this.price = '';
        this.query.pageNo = 1;
        this.getData();
        this.getAddTotal()
      },
      //当前页改变时
      handleCurrentChange(val) {
        this.query.pageNo = val;
        this.getData();
      },
      //每页条数改变
      handleSizeChange(val) {
        this.query.pageNo = 1;
        this.query.pageSize = val;
        this.getData()
      },
    },

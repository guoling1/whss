              <el-input style="width: 193px" v-model="query.receiptMoneyUserName" placeholder="请输入内容" size="small"></el-input>
            </li>
            <li class="same" v-if="isShow">
              <label>业务类型:</label>
              <el-select style="width: 193px" clearable v-model="query.businessType" size="small" >
                <el-option label="全部" value="">全部</el-option>
                <el-option label="好收收-收款" value="hssPay">好收收-收款</el-option>
                <el-option label="好收收-提现" value="hssWithdraw">好收收-提现</el-option>
                <el-option label="好收收-升级费" value="hssUpgrade">好收收-升级费</el-option>
                <el-option label="好收银-收款" value="hsyPay">好收银-收款</el-option>
              </el-select>
            </li>
            <li class="same" v-if="isShow">
              <label>分润日期:</label>
              <el-date-picker
                style="width: 193px"
                v-model="date"
                type="daterange"
                align="right"
                placeholder="选择日期范围"
                :picker-options="pickerOptions" size="small"
                :clearable="false"
                :editable="false" @change="datetimeSelect">
              </el-date-picker>
            </li>
            <li class="same">
              <div class="btn btn-primary" @click="search">筛选</div>
              <div class="btn btn-primary" @click="reset">重置</div>
            </li>
          </ul>
          <!--表格-->
          <el-table v-loading.body="loading" style="font-size: 12px;margin-bottom: 15px" :data="records" border>
            <el-table-column width="62" label="序号" fixed="left" type="index"></el-table-column>
            <el-table-column prop="splitSn" label="分润流水号" width="218"></el-table-column>
            <el-table-column prop="businessType" label="业务类型"></el-table-column>
            <el-table-column label="分润时间" width="152">
              <template scope="scope">
                <span>{{scope.row.splitDate|changeTime}}</span>
              </template>
            </el-table-column>
            <el-table-column prop="orderNo" label="交易订单号" width="218"></el-table-column>
            <el-table-column prop="settleType1" label="结算周期"></el-table-column>
            <el-table-column label="分润总额" align="right" min-width="90">
              <template scope="scope">
                <span>{{scope.row.splitTotalAmount|toFix}}</span>
              </template>
            </el-table-column>
            <el-table-column prop="splitAmount" align="right" header-align="left" label="分润金额">
              <template scope="scope">
                <span>{{scope.row.splitAmount|toFix}}</span>
              </template>
            </el-table-column>
            <el-table-column prop="outMoneyAccountName" label="分润出款账户"></el-table-column>
            <el-table-column prop="receiptMoneyUserName" label="分润方名称"></el-table-column>
            <el-table-column prop="profitType" label="分润方类型" v-if="isShow"></el-table-column>
            <el-table-column prop="remark" label="备注信息"></el-table-column>
          </el-table>
          <ul style="float: left;margin-top: 5px">
            <li>
              <label style="margin-right: 10px;">分润总额</label>
              <span>当页总额：{{pageTotal}}&nbsp;元</span>&nbsp;&nbsp;&nbsp;&nbsp;
              <span>统计总额：{{addTotal}}&nbsp;元</span>
            </li>
            <li>
              <label style="margin-right: 10px;">分润金额</label>
              <span>当页总额：{{pageTotal1}}&nbsp;元</span>&nbsp;&nbsp;&nbsp;&nbsp;
              <span>统计总额：{{addTotal1}}&nbsp;元</span>
            </li>
          </ul>
          <!--分页-->
          <div class="block" style="text-align: right">
            <el-pagination @size-change="handleSizeChange"
                           @current-change="handleCurrentChange"
                           :current-page="query.pageNo"
                           :page-sizes="[10, 20, 50]"
                           :page-size="query.pageSize"
                           layout="total, sizes, prev, pager, next, jumper"
                           :total="count">
            </el-pagination>
          </div>
        </div>
      </div>
